     private void loadData(String key){
        
        try {
            model.setRowCount(0); //menghapus data scra keseluruhan n diisi dg perintah yg baru
            
            String where = !key.isEmpty() ? "WHERE nama_barang LIKE '%"+key+"%' OR harga LIKE '%"+key+"%' OR kategori LIKE '%"+key+"%' OR stok LIKE '%"+key+"%' OR diskon LIKE '%"+key+"%' " : ""; //melakukan pengecekan
            String q = "SELECT * FROM barang " +where;
            //pembuatan koneksi
            Connection c = Koneksi.MySQL();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(q);
            int nomor = 0;
            while (rs.next()) {
                int id = rs.getInt("id_barang");
                String nama = rs.getString("nama_barang");
                String harga = rs.getString("harga");
                String kategori = rs.getString("kategori");
                String stok = rs.getString("stok");
                String diskon = rs.getString("diskon");
                nomor++;
                Object[] data = {id,nomor, nama, harga, kategori, stok, diskon};
                model.addRow(data);
                
            }
            
        } catch (Exception e) {
            //utk mnnngkap eror
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
    }

