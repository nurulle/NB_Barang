    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         // utk mnghapus yg dideteksi index dulu
        int baris = jTable1.getSelectedRow();
        if(baris != -1){
            //JOptionPane.showMessageDialog(this, baris); //ini igunakan urk melihat index jadi td berpengaruh
            int pilih = JOptionPane.showConfirmDialog(this, 
                    "Apakah anda yakin?", "Konfirmasi", 
                    JOptionPane.YES_NO_OPTION);
            if(pilih == 0){
                //hapus data
                try {
                    Connection c = Koneksi.MySQL();
                    Statement st = c.createStatement();
                    
                    Object oid = jTable1.getValueAt(baris, 0); //utk mndpatkan nialai pd index ke bari n kolom ke 0 return value brupa object hasilnya
                    String strid = oid.toString(); //untk mngkon versi objec ke string
                    int id = Integer.parseInt(strid); //utk konversi string ke int
                    String q = "DELETE FROM barang WHERE id_barang="+id+"";
                    st.executeUpdate(q);
                    
                    //utk refrest data
                    loadData("");
                    //utk pesan bahwa data sudah di hapus
                    JOptionPane.showMessageDialog(this, "Data telah dihapus!");
                    
                } catch (SQLException | NumberFormatException | HeadlessException e) {
                }
                
            }else if(pilih == 1){ //boleh langsusng mnggunakan else if atau else
                jTable1.clearSelection();
                
                
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

